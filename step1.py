"""
ШАГ 1. Кодируем инструкцию в байты ВРУЧНУЮ (без struct).

Запуск:  python3 step1.py

Цель: понять, что упаковка инструкции в байты — это просто арифметика
с битами. struct.pack делает ровно то же самое под капотом.

Формат инструкции — 4 байта = 32 бита, разложенные так:

   биты:  31 ......... 24 | 23 ... 20 | 19 ... 16 | 15 ........... 0
   поле:      опкод        |    rd     |    rs     |     константа
              (8 бит)      |  (4 бита) |  (4 бита) |     (16 бит)

То есть всё число — это одно 32-битное целое, в котором разные группы
битов означают разные вещи.
"""

# ---------------------------------------------------------------
#  Мини-напоминание про битовые операции (можешь запустить и посмотреть)
# ---------------------------------------------------------------
def demo_bits():
    x = 9              # 9 в двоичном виде это 1001
    print("9 в двоичном:", bin(9))          # 0b1001
    print("9 << 4      :", bin(9 << 4), "=", 9 << 4)  # сдвиг влево на 4 = умножение на 16
    print("0x12 & 0x0F :", 0x12 & 0x0F)     # & оставляет только младшие 4 бита
    print("0xA0 | 0x05 :", hex(0xA0 | 0x05))  # | склеивает биты


# ---------------------------------------------------------------
#  УПАКОВКА: четыре числа -> 4 байта
# ---------------------------------------------------------------
def encode(opcode, rd, rs, imm):
    """Собрать инструкцию в 4 байта вручную."""
    # 1) Превращаем 16-битную константу в беззнаковый вид.
    #    Если число отрицательное, берём его дополнение до 2^16.
    #    Так -1 станет 0xFFFF, и поместится в 16 бит.
    imm16 = imm & 0xFFFF

    # 2) Складываем всё в одно 32-битное число сдвигами:
    #    опкод занимает старшие 8 бит -> сдвигаем влево на 24
    #    rd  -> на 20, rs -> на 16, imm -> в младшие 16
    word = (opcode << 24) | (rd << 20) | (rs << 16) | imm16

    # 3) Разбиваем 32-битное число на 4 отдельных байта.
    #    >> сдвигает нужный байт вправо, & 0xFF отрезает только его.
    b0 = (word >> 24) & 0xFF   # самый старший байт
    b1 = (word >> 16) & 0xFF
    b2 = (word >> 8) & 0xFF
    b3 = word & 0xFF           # самый младший байт

    return bytes([b0, b1, b2, b3])


# ---------------------------------------------------------------
#  РАСПАКОВКА: 4 байта -> четыре числа
# ---------------------------------------------------------------
def decode(data):
    """Разобрать 4 байта обратно в (opcode, rd, rs, imm)."""
    # 1) Склеиваем 4 байта обратно в одно 32-битное число.
    word = (data[0] << 24) | (data[1] << 16) | (data[2] << 8) | data[3]

    # 2) Вырезаем каждое поле сдвигом и маской.
    opcode = (word >> 24) & 0xFF
    rd = (word >> 20) & 0x0F   # 0x0F = маска 4 бита
    rs = (word >> 16) & 0x0F
    imm = word & 0xFFFF

    # 3) Возвращаем константе знак: если старший (15-й) бит стоит,
    #    значит число было отрицательным -> вычитаем 2^16.
    if imm & 0x8000:
        imm -= 0x10000

    return opcode, rd, rs, imm


