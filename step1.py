if __name__ == "__main__":
    print("=== битовые операции ===")
    demo_bits()

    print("\n=== упаковка/распаковка ===")
    OPCODE_LI = 9
    packed = encode(OPCODE_LI, rd=1, rs=0, imm=42)
    print("LI R1, 42 -> hex:", packed.hex())

    op, rd, rs, imm = decode(packed)
    print("распаковали:", "опкод", op, "rd", rd, "rs", rs, "imm", imm)

    # проверим отрицательное число
    packed_neg = encode(OPCODE_LI, rd=2, rs=0, imm=-1)
    print("\nLI R2, -1 -> hex:", packed_neg.hex())
    print("распаковали imm:", decode(packed_neg)[3])